// Translation function
function translateElement(element, language) {
    const key = element.getAttribute('data-translate');
    if (key && translations[language] && translations[language][key]) {
        if (element.tagName === 'INPUT' && element.type === 'submit') {
            element.value = translations[language][key];
        } else if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
            element.placeholder = translations[language][key];
        } else if (element.hasAttribute('title')) {
            element.title = translations[language][key];
        } else if (element.hasAttribute('alt')) {
            element.alt = translations[language][key];
        } else {
            element.textContent = translations[language][key];
        }
    }
}

// Apply translations to the page
function applyTranslations(language) {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => translateElement(element, language));
    
    // Update document title
    const titleElement = document.querySelector('title[data-translate]');
    if (titleElement) {
        const key = titleElement.getAttribute('data-translate');
        if (translations[language] && translations[language][key]) {
            document.title = translations[language][key];
        }
    }
    
    // Update meta description
    const metaElement = document.querySelector('meta[data-translate="terms_meta_description"], meta[data-translate="privacy_meta_description"]');
    if (metaElement) {
        const key = metaElement.getAttribute('data-translate');
        if (translations[language] && translations[language][key]) {
            metaElement.setAttribute('content', translations[language][key]);
        }
    }
    
    // Update html lang attribute
    document.documentElement.lang = language;
}

// Get current language from localStorage or default to Spanish
function getCurrentLanguage() {
    return localStorage.getItem('InnovAI-language') || 'es';
}

// Set and save language preference
function setLanguage(language) {
    localStorage.setItem('InnovAI-language', language);
    applyTranslations(language);
}

// Initialize translations when page loads
document.addEventListener('DOMContentLoaded', function() {
    const currentLang = getCurrentLanguage();
    applyTranslations(currentLang);
});